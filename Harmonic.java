

import java.util.*;

class Harmonic{
	public static void main(String ar[])
	{
		Scanner sc=new Scanner(System.in);
		System.out.println("Enter Number-->");
		int n=sc.nextInt();
		float total=0;
		float f=0;
		for(float i=1;i<=n;i++)
	{	f=(1/i);
		total=total+f;
		
		
		}
	System.out.println(total);
	
	
	}
	
	
}
